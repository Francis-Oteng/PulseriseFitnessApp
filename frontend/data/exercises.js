// Helper functions
export const getExercisesByCategory = (category) =>
  exercises.filter((e) => e.category === category);

export const getExercisesByMuscleGroup = (muscleGroup) =>
  exercises.filter((e) => e.muscleGroup === muscleGroup);

export const getExercisesByDifficulty = (difficulty) =>
  exercises.filter((e) => e.difficulty === difficulty);

export const getExerciseById = (id) => exercises.find((e) => e.id === id);

export const searchExercises = (query) => {
  const q = query.toLowerCase();
  return exercises.filter(
    (e) =>
      e.name.toLowerCase().includes(q) ||
      e.category.toLowerCase().includes(q) ||
      e.muscleGroup.toLowerCase().includes(q) ||
      e.equipment.toLowerCase().includes(q)
  );
};

export default exercises;
